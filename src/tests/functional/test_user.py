def test_all_users(test_app, test_database, add_user):
    # remove all the rows in the table at the start of the test:
    test_database.session.query(User).delete()
    add_user("user-101", "user-101@cloudnua.io")
    add_user("user-102", "user-102@cloudnua.io")
    client = test_app.test_client()
    resp = client.get("/users")
    data = json.loads(resp.data.decode())
    assert resp.status_code == 200
    assert len(data) == 2
    assert "user-101" in data[0]["username"]
    assert "user-101@cloudnua.io" in data[0]["email"]
    assert "user-102" in data[1]["username"]
    assert "user-102@cloudnua.io" in data[1]["email"]
